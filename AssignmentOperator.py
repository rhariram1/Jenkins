#! /usr/bin/python
# A program to Assignment Operators - Add, Subtract, Multiply, Divide, modules, exponent and floor division operations
import math
a=20
b=10
print ("Addition Operations of 2 assigned numbers {} and {} is {}".format(a,b,a+b))
print ("Subtraction Operations of 2 assigned numbers {} and {} is {}".format(a,b,a-b))
print ("Multiplication Operations of 2 assigned numbers {} and {} is {}".format(a,b,a*b))
print ("Division Operations of 2 assigned numbers {} and {} is {}".format(a,b,a/b))
print ("Modules Operations on assigned numbers {} and {} is {}".format(a,b,a%b))
print ("Exponent Operations to power of 3 on assigned numbers {} and {} is {} {}".format(a,b,a**3,b**3))
c=2.37
print ("Floor of a assigned number {} is {}".format(c,math.floor(c)))
print ("Ceil of a assigned number {} is {}".format(c,math.ceil(c)))
